class BoxShadowPanel extends JPanel {
    @Override
    protected void paintComponent(Graphics g) {
        super.paintComponent(g);

        Graphics2D g2d = (Graphics2D) g;

        // Draw the content with border radius
        int borderRadius = 10;
        g2d.setColor(Color.WHITE);
        g2d.fillRoundRect(10, 10, getWidth() - 20, getHeight() - 20, borderRadius, borderRadius);

        // Draw the soft box shadow
        int shadowSize = 10;
        int shadowTransparency = 20; // Adjust this value for the transparency level

        for (int i = 0; i < shadowSize; i++) {
            float alpha = (float) (i + 1) / shadowSize * shadowTransparency / 100f;
            g2d.setColor(new Color(0, 0, 0, alpha));
            g2d.drawRoundRect(10 - i, 10 - i, getWidth() - 20 + 2 * i, getHeight() - 20 + 2 * i, borderRadius, borderRadius);
        }
    }
}

